
package pkg100.days.of.coding;
public class Day15_GanjilGenap {

    public static void main(String[] args) {
      // belajar ganjil dan genap
      
        for (int i = 0; i < 10; i++) {
            if (i % 2 == 1){
                System.out.println(i + " ganjil");
            }
        }
        for (int i = 0; i < 10; i++) {
            if (i % 2 == 0){
                System.out.println(i + " genap");
            }
        }
    }
    
}
