
public class OperatorAritmatika {
    
    public static void main(String[] args) {
        int angka = 4;
        int nilai = 9;
        
        int tambah = angka + nilai;
        int kurang = angka - nilai;
        int bagi = angka / nilai;
        int kali = angka * nilai;
        int sisabagi = angka % nilai;
        
        System.out.println("tambah =" + tambah);
        System.out.println("kurang =" + kurang);
        System.out.println("bagi =" + bagi);
        System.out.println("kali =" + kali);
        System.out.println("sisabagi =" + sisabagi);
    }
    
}	