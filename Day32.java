
package pkg100.days.of.coding;

public class Day32_pola15 {

    public static void main(String[] args) {
         // buat pola 15
      
        int S = 7;
          
        for (int i = 0; i < S; i++) {
            for (int j = 0; j <= i; j++) {
                System.out.print(" ");
            }
            System.out.println("*");
            
        }
        
     }
  
  }
 
