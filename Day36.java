
package pkg100.days.of.coding;

public class Day36_pola4 {

    public static void main(String[] args) {
        // pola 4
        
        int e = 5;
        
        for (int i = 1; i <= e; i++) {
            for (int j = 1; j <= i; j++) {
                System.out.print("* ");
            }
            System.out.println("");
        }
    }
    
}
