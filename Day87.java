package pkg100.days.of.coding;

public class Day87_ {
    
    public static void aritmatika(int a, int b){
        System.out.println(a + " + " + b + " = " + (a+b));
        System.out.println(a + " - " + b + " = " + (a-b));
        System.out.println(a + " x " + b + " = " + (a*b));
        System.out.println(a + " : " + b + " = " + (a/b));
 
    }
    
    public static void main(String[] args) {
       aritmatika(30,10);
    }
    
}
